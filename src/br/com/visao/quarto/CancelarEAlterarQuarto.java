    private void jBalterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBalterarActionPerformed
        // TODO add your handling code here:
        q.setTipo(jCtipo.getSelectedItem().toString());
        q.setQtdCama(Integer.valueOf(jCqtdCamas.getSelectedItem().toString()));
        q.setSoma(Double.valueOf(jLvalor.getText()));
        q.setCodigo(Integer.valueOf(jTcodigo.getText()));
        ManterQuarto dao = new ManterQuarto();
        try {
            dao.AtualizarQuarto(q);
        } catch (Exception ex) {
            Logger.getLogger(CancelarEAlterarQuarto.class.getName()).log(Level.SEVERE, null, ex);
        }   
    }//GEN-LAST:event_jBalterarActionPerformed

    private void jBcancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBcancelarActionPerformed
        // TODO add your handling code here:
        if(q.isReservado() == false) {
            q.setCodigo(Integer.valueOf(jTcodigo.getText()));
            ManterQuarto dao = new ManterQuarto();
            try {
                dao.deletarQuarto(q);
            } catch (Exception ex) {
                Logger.getLogger(CancelarEAlterarQuarto.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Quarto reservado, não é possível deletar");
        }
    }//GEN-LAST:event_jBcancelarActionPerformed

    private void jBimprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBimprimirActionPerformed
        // TODO add your handling code here:
        try {
            Quarto q = new Quarto();
            ManterQuarto dao = new ManterQuarto();
            DefaultTableModel quarto = (DefaultTableModel) jTquartos.getModel();
            quarto.setRowCount(0);
            {
                ArrayList<Quarto> quartos = dao.PesquisarQuarto();
                for (int i = 0; i < quartos.size(); i++) {
                    q = quartos.get(i);
                    quarto.addRow(new Object[]{q.getCodigo(), q.getTipo(), q.getQtdCama(), q.getSoma(), q.isReservado()});
                    System.out.println("q" + q.getCodigo() + q.getTipo() + q.getQtdCama() + q.getSoma() + q.isReservado());
                }
            }
        } catch (Exception e) {
            System.out.println("Erro " + e.getMessage());
        }
    }//GEN-LAST:event_jBimprimirActionPerformed

    private void jCtipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCtipoActionPerformed
        // TODO add your handling code here:

        if (null != jCtipo.getSelectedItem().toString()) switch (jCtipo.getSelectedItem().toString()) {
            case " " -> q.setValorTipo(0.0);
            case "normal" -> q.setValorTipo(50.00);
            case "master" -> q.setValorTipo(143.00);
            case "deluxe" -> q.setValorTipo(183.20);
        }
        jLvalor.setText(String.valueOf(q.somaValor()));
    }//GEN-LAST:event_jCtipoActionPerformed


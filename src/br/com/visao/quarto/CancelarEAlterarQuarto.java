    private void jTquartosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTquartosMouseClicked
        // TODO add your handling code here:
        DefaultTableModel tblModel = (DefaultTableModel)jTquartos.getModel();
        
        String tblCodigo = tblModel.getValueAt(jTquartos.getSelectedRow(), 0).toString();
        String tblTipo = tblModel.getValueAt(jTquartos.getSelectedRow(), 1).toString();
        String tblQtdCamas = tblModel.getValueAt(jTquartos.getSelectedRow(), 2).toString();
        String tblValor = tblModel.getValueAt(jTquartos.getSelectedRow(), 3).toString();
        
        jTcodigo.setText(tblCodigo);
        jCtipo.setSelectedItem(tblTipo);
        jCqtdCamas.setSelectedItem(tblQtdCamas);
        jLvalor.setText(tblValor);
    }//GEN-LAST:event_jTquartosMouseClicked

